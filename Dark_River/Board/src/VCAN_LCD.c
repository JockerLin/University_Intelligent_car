void LCD_Print(byte x, byte y, byte ch[])
{
	byte ch2[3];
	byte ii=0;        
	while(ch[ii] != '\0')
	{
		if(ch[ii] > 127)
		{
			ch2[0] = ch[ii];
	 		ch2[1] = ch[ii + 1];
			ch2[2] = '\0';			//汉字为两个字节
			LCD_P14x16Str(x , y, ch2);	//显示汉字
			x += 14;
			ii += 2;
		}
		else
		{
			ch2[0] = ch[ii];	
			ch2[1] = '\0';			//字母占一个字节
			//LCD_P8x16Str(x , y , ch2);	//显示字母
                        LCD_P6x8Str(x , y , ch2);
			x += 8;
			ii+= 1;
		}
	}
}
void DisplayFloat(int i,uint8 x,uint8 y)
{
      int8 a[7]={0}; 
      if(i<0)
      {
        a[0]='-';
        i=-i;
      }
      else
      a[0]='+';
      
      a[1]=i/10000;
      a[2]=i%10000/1000;
      a[3]='.';
      a[4]=i%1000/100;
      a[5]=i%100/10;
      a[6]=i%10;
      for(uint8 j=0;j<7;j++)
      {
         switch(a[j])
         {
           case 0:{LCD_P6x8Str(x,y,"0");}break;
           case 1:{LCD_P6x8Str(x,y,"1");}break;
           case 2:{LCD_P6x8Str(x,y,"2");}break;
           case 3:{LCD_P6x8Str(x,y,"3");}break;
           case 4:{LCD_P6x8Str(x,y,"4");}break;
           case 5:{LCD_P6x8Str(x,y,"5");}break;
           case 6:{LCD_P6x8Str(x,y,"6");}break;
           case 7:{LCD_P6x8Str(x,y,"7");}break;
           case 8:{LCD_P6x8Str(x,y,"8");}break;
           case 9:{LCD_P6x8Str(x,y,"9");}break; 
         case '-':{LCD_P6x8Str(x,y,"-");}break;
         case '+':{LCD_P6x8Str(x,y,"+");}break;
         case '.':{LCD_P6x8Str(x,y,".");}break;
           default:break;
          }
         x+=6;
         if(x==90)
           x=20;
         }
}

void LCD_PrintU16(unsigned char x,unsigned char y,unsigned int num)
{
	unsigned char tmp[6],i;
	tmp[5]=0;
	tmp[4]=(unsigned char)(num%10+0x30);
	tmp[3]=(unsigned char)(num/10%10+0x30);
	tmp[2]=(unsigned char)(num/100%10+0x30);
	tmp[1]=(unsigned char)(num/1000%10+0x30);
	tmp[0]=(unsigned char)(num/10000%10+0x30);
	
	for(i=0;i<4;i++)
	{
		if(tmp[i]=='0')
			tmp[i]=' ';
		else
			break;
	}
	
	LCD_P6x8Str(x,y,tmp);
}



